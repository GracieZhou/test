// Boot video issue, "mstar.videoadvert.finished" is
//  0 means that video does not finish
//  1 means that video has finished
//  no default value
//
// When there has a video file to be played, we has cleared screen
// , so users can see video behind screen.
// Bootanimation will wait for video to finish, before it exits.
// After bootanimation exits, users can see launcher UI.
//
// Return Value
// true means that a video file has been played
// false means that no video
bool BootAnimation::video() {
    char value[PROPERTY_VALUE_MAX] = {0};

    // clear screen
    glShadeModel(GL_FLAT);
    glDisable(GL_DITHER);
    glDisable(GL_SCISSOR_TEST);

    glClearColor(0,0,0,0);
    glClear(GL_COLOR_BUFFER_BIT);
    eglSwapBuffers(mDisplay, mSurface);

    // wait the video advert file to finish playing
    do {
        property_get("mstar.videoadvert.finished", value, "0");
        int exitnow = atoi(value);
        if (exitnow != 0) {
            break;
        }
        usleep(100000);
    } while (true);

    return false;
}
